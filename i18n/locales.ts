// Helper to set cookie
export const setLanguageCookie = (lang: Language) => {
	const d = new Date();
	d.setTime(d.getTime() + 365 * 24 * 60 * 60 * 1000); // 1 year
	let expires = 'expires=' + d.toUTCString();
	document.cookie = 'infinitum_lang=' + lang + ';' + expires + ';path=/';
};

// Helper to get cookie
export const getLanguageCookie = (): Language | null => {
	const name = 'infinitum_lang=';
	const decodedCookie = decodeURIComponent(document.cookie);
	const ca = decodedCookie.split(';');
	for (let i = 0; i < ca.length; i++) {
		let c = ca[i];
		while (c.charAt(0) == ' ') {
			c = c.substring(1);
		}
		if (c.indexOf(name) == 0) {
			const val = c.substring(name.length, c.length) as Language;
			return supportedLanguages.includes(val) ? val : null;
		}
	}
	return null;
};

export const getBrowserLanguage = (): Language => {
	// Check cookie first
	const cookieLang = getLanguageCookie();
	if (cookieLang) return cookieLang;

	// Fallback to browser language
	const browserLang = navigator.language.split('-')[0] as Language;
	return supportedLanguages.includes(browserLang) ? browserLang : 'en';
};

export const getLanguageName = (lang: Language): string => {
	return languageInfo[lang]?.name || 'English';
};

